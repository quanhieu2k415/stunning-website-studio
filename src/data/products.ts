/** Get ProductDetail by ID, with auto-generation fallback for products without manual detail */
export function getProductDetail(id: string): ProductDetail | undefined {
  if (productDetails[id]) return productDetails[id];

  // Fallback: generate basic detail from allProducts list
  const product = allProducts.find((p) => p.id === Number(id));
  if (!product) return undefined;

  return {
    ...product,
    images: [product.image],
    rating: 4.5,
    reviews: 0,
    categoryLabel: categoryLabelMap[product.category] || product.category,
    description: `${product.name} - sản phẩm chính hãng từ ${product.brand}. Liên hệ để được tư vấn chi tiết.`,
    features: [],
    specs: {},
    warranty: "12 tháng",
    inStock: true,
    sku: "",
    variants: [],
  };
}

